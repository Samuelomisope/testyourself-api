export const UNIVERSITY_NAMES = UNIVERSITIES.map(u => u.name);

export const getUniversity = (name) => UNIVERSITIES.find(u => u.name === name);

export const getFaculties = (universityName) => {
  const uni = getUniversity(universityName);
  return uni ? uni.faculties : [];
};

export const getDepartments = (universityName, facultyName) => {
  const faculties = getFaculties(universityName);
  const faculty = faculties.find(f => f.name === facultyName);
  return faculty ? faculty.departments : [];
};